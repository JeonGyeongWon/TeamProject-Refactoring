package hotel.dto;

public class RoomDTO {

	int h_no;
	int h_rno;
	int personne;
	int bed;
	int bathroom;
	String roomsize;
	int weekprice;
	int weekend_price;
	String imgpath;
	String imgname;
	
	
	public int getH_no() {
		return h_no;
	}
	public void setH_no(int h_no) {
		this.h_no = h_no;
	}
	public int getH_rno() {
		return h_rno;
	}
	public void setH_rno(int h_rno) {
		this.h_rno = h_rno;
	}
	public int getPersonne() {
		return personne;
	}
	public void setPersonne(int personne) {
		this.personne = personne;
	}
	public int getBed() {
		return bed;
	}
	public void setBed(int bed) {
		this.bed = bed;
	}
	public int getBathroom() {
		return bathroom;
	}
	public void setBathroom(int bathroom) {
		this.bathroom = bathroom;
	}
	public String getRoomsize() {
		return roomsize;
	}
	public void setRoomsize(String roomsize) {
		this.roomsize = roomsize;
	}
	public int getWeekprice() {
		return weekprice;
	}
	public void setWeekprice(int weekprice) {
		this.weekprice = weekprice;
	}
	public int getWeekend_price() {
		return weekend_price;
	}
	public void setWeekend_price(int weekend_price) {
		this.weekend_price = weekend_price;
	}
	public String getImgpath() {
		return imgpath;
	}
	public void setImgpath(String imgpath) {
		this.imgpath = imgpath;
	}
	public String getImgname() {
		return imgname;
	}
	public void setImgname(String imgname) {
		this.imgname = imgname;
	}
	
	
	
}
